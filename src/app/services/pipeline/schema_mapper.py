# ==================================================
# COLUMN STANDARDIZATION
# ==================================================

COLUMN_MAPPING = {

    # ==================================================
    # FECHAS
    # ==================================================

    "Fecha": "fecha",

    # ==================================================
    # CANAL
    # ==================================================

    "Canal": "canal",

    "Subcanal": "subcanal",

    "Cadena": "cadena",

    # ==================================================
    # AGENCIA
    # ==================================================

    "Agencia_ID": "agencia_id",

    "Agencia": "agencia",

    # ==================================================
    # PRODUCTO
    # ==================================================

    "Producto_ID": "producto_id",

    "Producto": "producto",

    # ==================================================
    # DEVOLUCIONES
    # ==================================================

    "Devolución $": "devolucion_clp",

    "Devolución Tn": "devolucion_tn",

    "Devolución UE": "devolucion_ue",

    "Devolución Pz": "devolucion_pz",

    # ==================================================
    # VENTAS
    # ==================================================

    "Venta Neta $": "venta_total",

    "Venta Neta TM": "venta_tn",

    "Venta Neta UE": "venta_ue",

    "Venta Neta Pz": "venta_pz"

}

# ==================================================
# NORMALIZE COLUMNS
# ==================================================

def normalize_columns(

    df

):

    # ==================================================
    # RENAME
    # ==================================================

    df = df.rename(

        columns=COLUMN_MAPPING

    )

    # ==================================================
    # CLEAN COLUMN NAMES
    # ==================================================

    df.columns = [

        col.strip().lower()

        for col in df.columns

    ]

    return df