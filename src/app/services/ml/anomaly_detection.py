import pandas as pd

from sklearn.ensemble import IsolationForest

# ==================================================
# ANOMALY DETECTION
# ==================================================

def detect_sales_anomalies(

    sales_df

):

    # ==================================================
    # PREPARE DATA
    # ==================================================

    df = sales_df.copy()

    df = df.sort_values(

        ["year", "month"]

    )

    # ==================================================
    # FEATURES
    # ==================================================

    features = df[[

        "venta_total"

    ]]

    # ==================================================
    # MODEL
    # ==================================================

    model = IsolationForest(

        contamination=0.08,
        random_state=42

    )

    # ==================================================
    # TRAIN
    # ==================================================

    model.fit(features)

    # ==================================================
    # PREDICT
    # ==================================================

    df["anomaly"] = model.predict(features)

    # ==================================================
    # CLEAN LABELS
    # ==================================================

    df["anomaly_label"] = df["anomaly"].map({

        1: "Normal",
        -1: "Anomalía"

    })

    # ==================================================
    # PERIOD
    # ==================================================

    df["periodo"] = (

        df["year"].astype(str)
        +
        "-"
        +
        df["month"].astype(str)

    )

    return df